package pilas;

public class Nodo {
	
	private int valor;
	
	private Nodo sig;
	
	private Nodo prex;

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	public Nodo getSig() {
		return sig;
	}

	public void setSig(Nodo sig) {
		this.sig = sig;
	}

	public Nodo getPrex() {
		return prex;
	}

	public void setPrex(Nodo prex) {
		this.prex = prex;
	}
	
	

}
